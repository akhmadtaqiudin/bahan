package example.enum;

public class UseDisplay {
  
  private String id;
  private Display display;
  
  public UseDisplay() {
  }

  public void setId(String i) {
    id = i;
  }

  public String getId() {
    return id;
  }

  public void setDisplay(Display e) {
    display = e;
  }

  public Display getDisplay() {
    return display;
  }
}