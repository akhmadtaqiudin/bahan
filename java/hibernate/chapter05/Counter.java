package example.count;

public class Counter {
  private String id; 
  private Integer ivalue;

  public Counter() {
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id; 
  }

  public Integer getIvalue() {
    return ivalue;
  }

  public void setIvalue(Integer value) { 
    ivalue = value;
  }
}
